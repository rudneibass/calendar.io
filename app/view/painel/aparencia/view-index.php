    let mesAtual = new Date().getMonth() + 1; // Mês atual (1-12)
    let anoAtual = new Date().getFullYear(); // Ano atual

    function carregarCalendario(incremento) {
        mesAtual += incremento;
        if (mesAtual < 1) {
            mesAtual = 12;
            anoAtual--;
        } else if (mesAtual > 12) {
            mesAtual = 1;
            anoAtual++;
        }

        // Faz requisição AJAX para obter o HTML do calendário
        $.ajax({
            url: 'calendario.php',
            method: 'GET',
            data: { mes: mesAtual, ano: anoAtual },
            success: function(response) {
                $('#calendario').html(response);
                $('#mesAno').text(new Date(anoAtual, mesAtual - 1).toLocaleString('pt-BR', { month: 'long', year: 'numeric' }));
            }
        });
    }

    // Carregar o calendário inicial
    $(document).ready(function() {
        carregarCalendario(0);
    });
